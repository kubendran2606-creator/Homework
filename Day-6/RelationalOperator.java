class RelationalOperator
{
    public static void main(String[] args) 
	{

        int a = 10;
		int b = 20;
		
		boolean greater = a>b;
		boolean lesser = a<b;
		boolean equal = a==b;
		boolean notequal = a!=b;

        
        System.out.println(greater);
		System.out.println(lesser);
		System.out.println(equal);
		System.out.println(notequal);
    }
}