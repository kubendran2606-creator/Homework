class LanguageSelection
{
	public static void main(String[] args)
	{
		int language = 4;
		
		switch(language)
		{
		case 1:
		System.out.println("Tamil");
		break;
		case 2:
		System.out.println("English");
		break;
		case 3:
		System.out.println("Hindi");
		break;
		case 4:
		System.out.println("Malayalam");
		break;
		default :
		System.out.println("Invalaid language");
		}
	}
}